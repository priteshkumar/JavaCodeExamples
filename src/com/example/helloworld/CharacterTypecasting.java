package com.example.helloworld;

public class CharacterTypecasting {
    public static void main(String[] args) {
        char a = 70;
        System.out.println(a);
        a = (char) (a + 1);
        System.out.println(a);
        a = 'a' +1;
        System.out.println(a);
        a = (char)('a' + a);
        System.out.println(a);

    }
}
